    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        doPost(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
             // 设置响应内容类型  
        response.setContentType("text/html;charset=utf-8");
        request.setCharacterEncoding("utf-8");
        response.setCharacterEncoding("utf-8");

        try (PrintWriter out = response.getWriter()) {

            //获得请求中传来的String studentNo,String name, String college,String IDnumber
            int studentNo = Integer.parseInt(request.getParameter("studentNo").trim());
            String name = request.getParameter("name").trim();
            String college = request.getParameter("college").trim();
            String IDnumber = request.getParameter("IDnumber").trim();
//            int studentNo = 180400201;
//            String name = "耿延睿";
//            String college = "哈尔滨工业大学（威海）";
//            String IDnumber = "220106200004192013";

            //身份验证结果
            User result = UserDAO.stateUser(studentNo, name, college, IDnumber);

            Map<String, String> params = new HashMap<>();
            JSONObject jsonObject = new JSONObject();
            int state = result.getState();
            params.put("Result", String.valueOf(state));
            if(state == 0)
            {
                params.put("Sex", result.getSex());
                params.put("Nation", result.getNation());
                params.put("Grade", result.getGrade());
                params.put("Major", result.getMajor());
            }
            jsonObject.put("params2", params);
//            jsonObject.put("params2", params);
            out.write(jsonObject.toString());
        }
    }

